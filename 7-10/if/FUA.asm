.MODEL SMALL
.STACK
.DATA
	str0 db "MENU [a] BLUE [b] RED$"
	str1 db "Enter a choice: $"
	str2 db "You chose BLUE.$"
	str3 db "You chose RED.$"
	
.CODE
START:
	MOV AX, @DATA
	MOV DS, AX
	
	; BACKGROUND
	MOV AX, 0600H
	MOV BH, 07H
	MOV CX, 0000H
	MOV DX, 184FH
	INT 10H
	
	; CURSOR POS FOR MENU
	MOV AH, 02H
	MOV BH, 00
	MOV DX, 0C1DH
	INT 10H
	
	; PRINT MENU
	MOV AH, 09H
	LEA DX, STR0
	INT 21H
	
	; MOVE CURSOR FOR CHOICE
	MOV AH, 02H
	MOV BH, 00
	MOV DX, 0D1FH
	INT 10H
	
	; PRINT CHOICE
	MOV AH, 09H
	LEA DX, STR1
	INT 21H
	
	; ACCEPT INPUT
	MOV AH, 01H
	INT 21H
	
	; MOVE CURSOR FOR INPUT
	MOV AH, 02H
	MOV BH, 00
	MOV DX, 0E20H
	INT 10H
	
	CMP AL, 61H
	JE BLUE
	JMP RED

BLUE:	
	; PRINT BLUE
	MOV AH, 09H
	LEA DX, STR2
	INT 21H
	JMP EXIT
	
RED:
	; PRINT RED
	MOV AH, 09H
	LEA DX, STR3
	INT 21H
	
EXIT:
	MOV AH, 4CH
	INT 21H
END START