.MODEL SMALL
.STACK
.DATA
	msg1 db "Enter your middle initial: $"
	msg2 db "Your middle initial is $"
	choice db 0
.CODE
START:
	MOV AX, @DATA
	MOV DS, AX

	; SET RED BG WHITE FG
	MOV AX, 0600H
	MOV BH, 47H
	MOV CX, 0000H
	MOV DX, 0C27H
	INT 10H

	; SET GREEN BG WHITE FG
	MOV AX, 0600H
	MOV BH, 27H
	MOV CX, 0D00H
	MOV DX, 1827H
	INT 10H

	; SET BLUE
	MOV AX, 0600H
	MOV BH, 11H
	MOV CX, 0028H
	MOV DX, 184FH
	INT 10H

	; MOVE CURSOR FOR MSG1
	MOV AH, 02H
	MOV BH, 00
	MOV DX, 0B00H
	INT 10H

	; PRINT MSG1
	MOV AH, 09H
	LEA DX, MSG1
	INT 21H

	; ACCEPT CHARACTER
	MOV AH, 01H
	INT 21H
	MOV CHOICE, AL

	; MOVE CURSOR FOR MSG2
	MOV AH, 02H
	MOV BH, 00
	MOV DX, 0E00H
	INT 10H

	; PRINT MSG2
	MOV AH, 09H
	LEA DX, MSG2
	INT 21H

	; PRINT CHOICE
	; MOVE CHOICE TO DL
	MOV DL, CHOICE
	MOV AH, 02H
	INT 21H

	; TERMINATE
	MOV AH, 4CH
	INT 21H

END START